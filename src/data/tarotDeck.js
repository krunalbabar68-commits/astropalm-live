/**
 * Complete 78-card tarot deck
 */
export const COMPLETE_TAROT_DECK = [
  ...MAJOR_ARCANA,
  ...WANDS,
  ...CUPS,
  ...SWORDS,
  ...PENTACLES,
];

// ============================================
// UTILITY FUNCTIONS
// ============================================

/**
 * Get card by ID
 * @param {string} id - Card ID
 * @returns {Object|null} Card or null
 */
export const getCardById = (id) => {
  return COMPLETE_TAROT_DECK.find(card => card.id === id) || null;
};

/**
 * Get random card
 * @param {boolean} allowReversed - Allow reversed cards
 * @returns {Object} Random card with reversed property
 */
export const getRandomCard = (allowReversed = true) => {
  const randomIndex = Math.floor(Math.random() * COMPLETE_TAROT_DECK.length);
  const card = COMPLETE_TAROT_DECK[randomIndex];
  const reversed = allowReversed ? Math.random() > 0.5 : false;
  
  return {
    ...card,
    reversed,
  };
};

/**
 * Shuffle deck and draw multiple cards
 * @param {number} count - Number of cards to draw
 * @param {boolean} allowReversed - Allow reversed cards
 * @returns {Array} Array of drawn cards
 */
export const drawCards = (count = 1, allowReversed = true) => {
  const shuffled = [...COMPLETE_TAROT_DECK].sort(() => Math.random() - 0.5);
  const drawn = shuffled.slice(0, count);
  
  return drawn.map(card => ({
    ...card,
    reversed: allowReversed ? Math.random() > 0.5 : false,
  }));
};

/**
 * Get cards by suit
 * @param {string} suit - Suit name (wands, cups, swords, pentacles)
 * @returns {Array} Cards of that suit
 */
export const getCardsBySuit = (suit) => {
  return COMPLETE_TAROT_DECK.filter(card => card.suit === suit);
};

/**
 * Get cards by arcana
 * @param {string} arcana - 'major' or 'minor'
 * @returns {Array} Cards of that arcana
 */
export const getCardsByArcana = (arcana) => {
  return COMPLETE_TAROT_DECK.filter(card => card.arcana === arcana);
};

/**
 * Search cards by keyword
 * @param {string} keyword - Keyword to search
 * @returns {Array} Matching cards
 */
export const searchCards = (keyword) => {
  const term = keyword.toLowerCase();
  return COMPLETE_TAROT_DECK.filter(card => 
    card.name.toLowerCase().includes(term) ||
    card.keywords.some(k => k.toLowerCase().includes(term)) ||
    card.meaning.toLowerCase().includes(term)
  );
};

